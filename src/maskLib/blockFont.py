GLYPH_COLS = 5   # pixel columns per glyph
GLYPH_ROWS = 7   # pixel rows per glyph
CHAR_ADVANCE = GLYPH_COLS + 1   # columns from one character start to the next


def block_text_rects(text, height, origin=(0, 0)):
    '''
    (position, width, height) rectangles for text drawn in the block font.

    height is the character height (7 pixel rows); origin is the bottom-left
    of the first character. Unknown characters render as '?'. Each rectangle
    is one horizontal run of set pixels -- rectangles never nest.
    '''
    px = height / GLYPH_ROWS
    rects = []
    for k, char in enumerate(text):
        cols = FONT_5X7.get(char, FONT_5X7['?'])
        cx = origin[0] + k * CHAR_ADVANCE * px
        for row in range(GLYPH_ROWS):           # 0 = top row (bit 0)
            col = 0
            while col < GLYPH_COLS:
                if cols[col] & (1 << row):
                    run = col
                    while run < GLYPH_COLS and cols[run] & (1 << row):
                        run += 1
                    rects.append(((cx + col * px,
                                   origin[1] + (GLYPH_ROWS - 1 - row) * px),
                                  (run - col) * px, px))
                    col = run
                else:
                    col += 1
    return rects


def block_text_width(text, height):
    '''total width of text in the block font (no trailing spacing column)'''
    px = height / GLYPH_ROWS
    return (len(text) * CHAR_ADVANCE - 1) * px if text else 0


def add_block_label(chip, text, position, height=10, layer='LABELS'):
    '''
    Block-font drop-in for Chip.add_chip_label: draws text centered at
    position as closed rectangle polylines on the given layer. Safe for
    fill/boolean processing -- no outline ever encloses another.
    '''
    x0 = position[0] - block_text_width(text, height) / 2
    y0 = position[1] - height / 2
    for (rx, ry), rw, rh in block_text_rects(text, height, origin=(x0, y0)):
        chip.chipBlock.add(dxf.polyline(
            [(rx, ry), (rx + rw, ry), (rx + rw, ry + rh), (rx, ry + rh)],
            layer=layer, flags=1))
